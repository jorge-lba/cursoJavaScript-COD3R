/**
 * Operações - Atribuição
 */

 const valueA = 7;
 let valueB = 3;

 valueB += valueA;  // valueB = valueB + valueA
console.log(valueB);

 valueB -= 4;       // valueB = valueB - 4
console.log(valueB);

valueB *= 2;        // valueB = valueB * 2
console.log(valueB);

valueB /= 2;        // valueB = valueB / 2
console.log(valueB);

valueB %= 2;        // valueB = valueB % 2
console.log(valueB);